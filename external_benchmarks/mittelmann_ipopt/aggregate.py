#!/usr/bin/env python3
"""Aggregate results/{ma57,feral}.jsonl into REPORT.md."""
from __future__ import annotations

import json
from pathlib import Path

HERE = Path(__file__).resolve().parent
RESULTS = HERE / "results"
REPORT = HERE / "REPORT.md"

OPT_STATUSES = {
    "Optimal Solution Found",
    "Solved To Acceptable Level",
}


def load(solver: str) -> dict[str, dict]:
    path = RESULTS / f"{solver}.jsonl"
    if not path.exists():
        return {}
    rows = {}
    for line in path.read_text().splitlines():
        if not line.strip():
            continue
        r = json.loads(line)
        rows[r["problem"]] = r
    return rows


def classify(r: dict | None) -> str:
    if r is None:
        return "missing"
    if r["timed_out"]:
        return "timeout"
    if r["status"] in OPT_STATUSES:
        return "ok"
    return "fail"


def main() -> int:
    ma57 = load("ma57")
    feral = load("feral")
    problems = sorted(set(ma57) | set(feral))

    lines: list[str] = []
    lines.append("# Mittelmann Ipopt comparison: MA57 vs FERAL\n")
    lines.append("Generated by `aggregate.py` from `results/{ma57,feral}.jsonl`.")
    lines.append("Timeout: 600 s wall per problem.\n")

    counts = {"ma57": {"ok": 0, "fail": 0, "timeout": 0, "missing": 0},
              "feral": {"ok": 0, "fail": 0, "timeout": 0, "missing": 0}}
    for p in problems:
        counts["ma57"][classify(ma57.get(p))] += 1
        counts["feral"][classify(feral.get(p))] += 1

    lines.append("## Summary\n")
    lines.append("| solver | ok | fail | timeout | missing |")
    lines.append("|---|---:|---:|---:|---:|")
    for s in ("ma57", "feral"):
        c = counts[s]
        lines.append(f"| {s} | {c['ok']} | {c['fail']} | {c['timeout']} | {c['missing']} |")
    lines.append("")

    head_wins, tail_wins, both_ok = [], [], []
    for p in problems:
        m, f = ma57.get(p), feral.get(p)
        cm, cf = classify(m), classify(f)
        if cm == "ok" and cf == "ok":
            both_ok.append((p, m, f))
        if cm == "ok" and cf != "ok":
            head_wins.append((p, m, f, cf))
        if cf == "ok" and cm != "ok":
            tail_wins.append((p, m, f, cm))

    if both_ok:
        lines.append("## Both solved (sorted by feral/ma57 IPM ratio)\n")
        lines.append("| problem | ma57 iters | ma57 IPM (s) | feral iters | feral IPM (s) | ratio |")
        lines.append("|---|---:|---:|---:|---:|---:|")
        def ratio(row):
            _, m, f = row
            mt, ft = m.get("ipm_seconds") or 0.0, f.get("ipm_seconds") or 0.0
            return (ft / mt) if mt > 0 else float("inf")
        for p, m, f in sorted(both_ok, key=ratio, reverse=True):
            mt = m.get("ipm_seconds")
            ft = f.get("ipm_seconds")
            r = (ft / mt) if (mt and mt > 0 and ft is not None) else None
            lines.append(
                f"| {p} | {m.get('iters')} | {mt} | {f.get('iters')} | {ft} | "
                f"{f'{r:.2f}' if r is not None else '-'} |"
            )
        lines.append("")

    if head_wins:
        lines.append("## MA57 only\n")
        lines.append("| problem | feral status |")
        lines.append("|---|---|")
        for p, _m, f, _ in head_wins:
            status = "TIMEOUT" if (f and f["timed_out"]) else (f["status"] if f else "missing")
            lines.append(f"| {p} | {status} |")
        lines.append("")

    if tail_wins:
        lines.append("## FERAL only\n")
        lines.append("| problem | ma57 status |")
        lines.append("|---|---|")
        for p, m, _f, _ in tail_wins:
            status = "TIMEOUT" if (m and m["timed_out"]) else (m["status"] if m else "missing")
            lines.append(f"| {p} | {status} |")
        lines.append("")

    REPORT.write_text("\n".join(lines) + "\n")
    print(f"wrote {REPORT}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
